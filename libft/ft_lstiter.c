#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*ptr;

	ptr = lst;
	if (lst != NULL || f != NULL)
		while (ptr)
		{
			(*f)(ptr);
			ptr = ptr->next;
		}
}
