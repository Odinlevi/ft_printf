#include "libftprintf.h"

size_t	ft_nbrlen(int nbr)
{
	size_t i;

	i = 1;
	if (nbr < 0)
		nbr = -nbr;
	while (nbr / 10)
	{
		nbr /= 10;
		i++;
	}
	return (i);
}
